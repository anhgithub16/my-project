    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String where = "1";
        int tuyChonTimKiem = comboBox_tuyChonTimKiem.getSelectedIndex();
        
        switch(tuyChonTimKiem) {
            case 0:
                where = "idKH = " + txt_tuKhoa.getText();
            break;
            
            case 1:
                where = "soCongTo = " + txt_tuKhoa.getText();
            break;
        }
        
        label_maKH.setText("..");
        label_hoTenKH.setText("..");
        label_soCongTo.setText("..");
        label_soCMT.setText("..");
        label_soDienThoai.setText("..");
        label_email.setText("..");
        label_diaChi.setText("..");
        label_phuongCuTru.setText("..");
        
        String columns[] = new String[] {"idKH", "hoTen", "soCongTo", "soCMT", "soDienThoai", "email", "diaChi", "tenPhuong"};
        String table = "khachhang";
        
        String data[][] = DB_MySQL.select(columns, table, where);
        
        if(data != null) {
            
            label_maKH.setText(data[0][0]);
            label_hoTenKH.setText(data[0][1]);
            label_soCongTo.setText(data[0][2]);
            label_soCMT.setText(data[0][3]);
            label_soDienThoai.setText(data[0][4]);
            label_email.setText(data[0][5]);
            label_diaChi.setText(data[0][6]);
            label_phuongCuTru.setText(data[0][7]);
            
        } else 
            JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng yêu cầu !", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

