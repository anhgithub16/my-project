    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        Calendar c = Calendar.getInstance();
        Date date = c.getTime();
        
        if(Check_String.check(txt_chiSoMoi.getText(), Check_String.NUMBER)) {
            
            if(label_maKH.getText() != "...") {
            
                if(DB_MySQL.select(new String[] {"chiSoMoi"}, "chisodien", "idKH = " + label_maKH.getText())[0][0].charAt(0) == '0' && DB_MySQL.select(new String[] {"chiSoMoi"}, "datenhapchiso", "idKH = " + label_maKH.getText())[0][0].equals("0000-00-00")) {

                    if((Float.parseFloat(txt_chiSoMoi.getText()) > 0)  && (Float.parseFloat(txt_chiSoMoi.getText()) > Float.parseFloat(DB_MySQL.select(new String[] {"chiSoCu"}, "chisodien", "idKH = " + label_maKH.getText())[0][0]))) {

                        if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm chỉ số điện ?", "Xác nhận", JOptionPane.YES_OPTION) == JOptionPane.YES_OPTION) {

                            DB_MySQL.update("chisodien", new String[] {"chiSoMoi"}, new String[] {txt_chiSoMoi.getText()}, "idKH = " + label_maKH.getText());
                            DB_MySQL.update("datenhapchiso", new String[] {"chiSoMoi"}, new String[] {df.format(date)}, "idKH = " + label_maKH.getText());
                            JOptionPane.showMessageDialog(this, "Thêm chỉ số mới thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                            txt_chiSoMoi.setText("");

                        }

                    } else {
                        JOptionPane.showMessageDialog(this, "Chỉ số mới phải > 0 và > " + Float.parseFloat(DB_MySQL.select(new String[] {"chiSoCu"}, "chisodien", "idKH = " + label_maKH.getText())[0][0]) + " (chỉ số cũ)", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Khách hàng đã tồn tại chỉ số điện mới " + DB_MySQL.select(new String[] {"chiSoMoi"}, "datenhapchiso", "idKH = " + label_maKH.getText())[0][0], "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Chưa có khách hàng", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }
            
        } else
            JOptionPane.showMessageDialog(this, "Chỉ số điện mới phải là số !", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_jButton2ActionPerformed

