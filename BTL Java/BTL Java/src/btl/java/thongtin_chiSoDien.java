    private void showThongTin(String idKH) {
        
        String columns[] = new String[] {"hoTen", "soCongTo", "soCMT", "soDienThoai", "email", "tenPhuong", "diaChi", "chisodien.chiSoMoi", "datenhapchiso.chiSoMoi"};
        String table = "khachhang, chisodien, datenhapchiso";
        String where = "khachhang.idKH = chisodien.idKH AND chisodien.idKH = datenhapchiso.idKH AND khachhang.idKH = " + idKH;
        
        String data[][] = DB_MySQL.select(columns, table, where);
        label_maKH.setText(idKH);
        label_hoTen.setText(data[0][0]);
        label_soCongTo.setText(data[0][1]);
        label_soCMT.setText(data[0][2]);
        label_soDienThoai.setText(data[0][3]);
        label_email.setText(data[0][4]);
        label_phuongCuTru.setText(data[0][5]);
        label_diaChi.setText(data[0][6]);
        txt_chiSoMoi.setText(data[0][7]);
        txt_chiSoMoi.setEnabled(false);
        label_thoiGianNhap.setText(data[0][8]);
        
    }
    
