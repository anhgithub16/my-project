    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(Check_String.check(txt_chiSoMoi.getText(), Check_String.NUMBER)) {
         
            float chiSoCu = Float.parseFloat(DB_MySQL.select(new String[] {"chiSoCu"}, "chisodien", "idKH = " + label_maKH.getText())[0][0]);
        
            if(Float.parseFloat(txt_chiSoMoi.getText()) > 0 && Float.parseFloat(txt_chiSoMoi.getText()) > chiSoCu) {

                if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn lưu thay đổi ?", "Cảnh báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                    String tables = "chisodien, datenhapchiso";
                    String columns[] = new String[] {"chisodien.chiSoMoi", "datenhapchiso.chiSoMoi"};
                    String where = "chisodien.idKH = datenhapchiso.idKH AND chisodien.idKH = " + label_maKH.getText();

                    DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                    Calendar c = Calendar.getInstance();
                    Date date = c.getTime();
                    String dateNow = df.format(date);

                    String values[] = new String[] {txt_chiSoMoi.getText(), dateNow};

                    DB_MySQL.update(tables, columns, values, where);

                    JOptionPane.showMessageDialog(this, "Cập nhật chỉ số điện thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

                    showThongTin(frameThongTinChiSoDien.idKH);

                }
            } else {
                JOptionPane.showMessageDialog(this, "Chỉ số mới phải > 0 và > " + chiSoCu + " (chỉ số cũ)", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }
            
        } else 
            JOptionPane.showMessageDialog(this, "Chỉ số mới phải là số !", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

